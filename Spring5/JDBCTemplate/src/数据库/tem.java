package 数据库;

public class tem {
    private String user;
    private String pass;

    public tem(String user, String pass) {
        this.user = user;
        this.pass = pass;
    }

    public tem() {
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
